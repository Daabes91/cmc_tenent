package com.clinic.modules.saas.model;

/**
 * Enum representing subscription plan tiers.
 * Each tier corresponds to a different level of service with specific features and pricing.
 */
public enum PlanTier {
    /**
     * Basic tier - Entry level plan with essential features
     */
    BASIC,
    
    /**
     * Professional tier - Mid-level plan with advanced features
     */
    PROFESSIONAL,
    
    /**
     * Enterprise tier - Full-featured plan for large organizations
     */
    ENTERPRISE,
    
    /**
     * Custom tier - Tailored plan with negotiated features and pricing
     */
    CUSTOM
}
