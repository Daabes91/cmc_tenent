package com.clinic.modules.ecommerce.model;

/**
 * Enumeration of product types.
 * 
 * Defines the different types of products supported in the e-commerce system.
 */
public enum ProductType {
    /**
     * Simple product with no variants.
     */
    SIMPLE,
    
    /**
     * Variable product with multiple variants (size, color, etc.).
     */
    VARIABLE
}