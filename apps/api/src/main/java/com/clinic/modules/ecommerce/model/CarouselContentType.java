package com.clinic.modules.ecommerce.model;

/**
 * Enumeration of carousel content types.
 * 
 * Defines the different types of content that can be displayed in carousel items.
 */
public enum CarouselContentType {
    /**
     * Image content with optional text overlay.
     */
    IMAGE,
    
    /**
     * Product showcase content.
     */
    PRODUCT,
    
    /**
     * Category showcase content.
     */
    CATEGORY,
    
    /**
     * Brand showcase content.
     */
    BRAND,
    
    /**
     * Special offer content.
     */
    OFFER,
    
    /**
     * Customer testimonial content.
     */
    TESTIMONIAL,
    
    /**
     * Blog post content.
     */
    BLOG
}