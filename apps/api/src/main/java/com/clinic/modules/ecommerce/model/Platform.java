package com.clinic.modules.ecommerce.model;

/**
 * Enumeration of platform targeting options.
 * 
 * Defines which platforms content should be displayed on.
 */
public enum Platform {
    /**
     * Web platform only.
     */
    WEB,
    
    /**
     * Mobile platform only.
     */
    MOBILE,
    
    /**
     * Both web and mobile platforms.
     */
    BOTH
}